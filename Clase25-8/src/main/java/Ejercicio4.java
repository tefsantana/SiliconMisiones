public class Ejercicio4 {
    /*Realizar un programa que lleve a cabo la cuenta regresiva para el año 
    nuevo. La cuenta debe comenzar en 10 y terminar en 1.*/
    public static void main(String[] args) {
        int i;
        for(i = 10; i >= 1; i--) {
            System.out.println(i);
        }
    }
}

